package com.example.lab5_ex.model;

public class Pet {

    // Private fields to store the pet's name and species
    private String name;
    private String species;

    // Getter method for the name field
    public String getName() {
        return name;
    }

    // Setter method for the name field
    public void setName(String name) {
        this.name = name;
    }

    // Getter method for the species field
    public String getSpecies() {
        return species;
    }

    // Setter method for the species field
    public void setSpecies(String species) {
        this.species = species;
    }
}
